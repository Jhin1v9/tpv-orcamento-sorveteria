create extension if not exists pgcrypto;

-- --- 1. CATEGORIAS DO CARDÁPIO REAL -------------------------------
create table if not exists public.product_categories (
  id text primary key,
  nome jsonb not null,
  emoji text not null default '🍨',
  display_order integer not null default 0,
  active boolean not null default true
);

-- --- 1b. CATEGORIAS LEGADAS (compatibilidade com pedidos antigos) -
create table if not exists public.categories (
  id text primary key,
  nome jsonb not null,
  base_price numeric(10,2) not null,
  max_flavors integer not null,
  cor_hex text not null,
  active boolean not null default true,
  display_order integer not null,
  image_url text not null,
  badge text
);

-- --- 2. PRODUTOS (fixos e personalizáveis) ------------------------
create table if not exists public.products (
  id text primary key,
  nome jsonb not null,
  descricao jsonb,
  badge jsonb,
  preco numeric(10,2),              -- para produtos fixos
  preco_base numeric(10,2),         -- para produtos personalizáveis
  imagem text not null,
  categoria_id text not null references public.product_categories(id),
  em_estoque boolean not null default true,
  alergenos jsonb not null default '[]'::jsonb,
  is_personalizavel boolean not null default false,
  opcoes jsonb not null default '{}'::jsonb,    -- { tamanhos:[], sabores:[], toppings:[], frutas:[], extras:[] }
  limites jsonb,                                -- { maxToppings, maxFrutas, maxSabores }
  active boolean not null default true,
  display_order integer not null default 0,
  created_at timestamptz not null default now()
);

-- --- 3. SABORES ARTESANAIS (estoque) ------------------------------
create table if not exists public.flavors (
  id text primary key,
  nome jsonb not null,
  categoria text not null,          -- 'cremoso', 'chocolate', 'especial', 'fruta', 'sorbete', 'frutos_secos'
  cor_hex text not null,
  image_url text not null,
  extra_price numeric(10,2) not null default 0,
  stock_buckets numeric(10,3) not null default 0,
  low_stock_threshold numeric(10,3) not null default 1,
  available boolean not null default true,
  badge text
);

-- --- 4. TOPPINGS --------------------------------------------------
create table if not exists public.toppings (
  id text primary key,
  nome jsonb not null,
  price numeric(10,2) not null,
  categoria text not null,          -- 'cobertura', 'crema', 'crocante', 'decoracion', 'mix-in'
  emoji text
);

-- --- 5. CONFIGURAÇÕES DA LOJA -------------------------------------
create table if not exists public.store_settings (
  store_key text primary key default 'main',
  name text not null,
  nif text not null,
  address text not null,
  summer_hours text not null,
  winter_hours text not null,
  updated_at timestamptz not null default now()
);

-- --- 6. CLIENTES --------------------------------------------------
create table if not exists public.customers (
  id uuid primary key default gen_random_uuid(),
  nome text not null,
  telefone text unique,
  email text,
  alergias jsonb not null default '[]'::jsonb,
  total_gasto numeric(10,2) not null default 0,
  pedidos_count integer not null default 0,
  criado_em timestamptz not null default now()
);

-- --- 7. PEDIDOS ---------------------------------------------------
create table if not exists public.orders (
  id uuid primary key default gen_random_uuid(),
  numero_sequencial bigint generated by default as identity unique,
  status text not null default 'pendiente',
  created_at timestamptz not null default now(),
  ready_at timestamptz,
  payment_method text not null,
  subtotal numeric(10,2) not null default 0,
  discount numeric(10,2) not null default 0,
  extras numeric(10,2) not null default 0,
  total numeric(10,2) not null,
  iva numeric(10,2) not null,
  verifactu_qr text,
  customer_phone text,
  customer_id uuid references public.customers(id),
  origem text not null default 'tpv' check (origem in ('tpv', 'kiosk', 'pwa')),
  nome_usuario text,
  notes text
);

-- --- 8. ITENS DE PEDIDO (genérico) --------------------------------
create table if not exists public.order_items (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.orders(id) on delete cascade,
  sort_order integer not null default 0,

  -- Tipo do item
  item_type text not null default 'product' check (item_type in ('product', 'legacy')),

  -- Campos para produtos (novo modelo)
  product_id text references public.products(id),
  product_name text,
  product_snapshot jsonb not null default '{}'::jsonb,
  selections jsonb not null default '[]'::jsonb,

  -- Campos legados (mantidos para dados históricos / compatibilidade)
  category_sku text,
  category_name text,
  flavors jsonb not null default '[]'::jsonb,
  toppings jsonb not null default '[]'::jsonb,

  -- Comuns
  unit_price numeric(10,2) not null,
  quantity integer not null default 1,
  notes text
);

-- --- 9. LOG DE ESTOQUE (auditoria) --------------------------------
create table if not exists public.inventory_log (
  id uuid primary key default gen_random_uuid(),
  flavor_id text not null references public.flavors(id),
